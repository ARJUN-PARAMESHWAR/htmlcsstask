function dropmenu() {


    if (document.getElementsByClassName("sidePanel")[0].style.display == "none") {
        document.getElementsByClassName("sidePanel")[0].style.display = "block";
    }

    else {
        document.getElementsByClassName("sidePanel")[0].style.display = "none";
    }

}

